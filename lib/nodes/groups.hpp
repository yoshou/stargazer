/**
 * @file groups.hpp
 * @brief Doxygen group definitions for stargazer node categories.
 */

/**
 * @defgroup calibration_nodes Calibration Nodes
 * @brief Nodes responsible for camera calibration workflows.
 *
 * These nodes perform intrinsic/extrinsic parameter estimation,
 * target detection, and scene calibration using DUSt3R or conventional methods.
 */

/**
 * @defgroup core_nodes Core Nodes
 * @brief General-purpose processing and utility nodes.
 *
 * These nodes handle tasks such as rendering, gating, property extraction,
 * keypoint conversion, and frame management.
 */

/**
 * @defgroup io_nodes I/O Nodes
 * @brief Nodes for loading and storing data from/to persistent storage.
 *
 * These nodes read from and write to SQLite databases, file systems,
 * and external data sources.
 */

/**
 * @defgroup reconstruction_nodes Reconstruction Nodes
 * @brief Nodes for 3D reconstruction and pose estimation.
 *
 * These nodes implement multi-view reconstruction algorithms including
 * epipolar geometry, VoxelPose, MVPose, and MVP methods.
 */
